//-----------------------------------------------------------------

void ADF4351_Wdata(uint32_t dat)
{
	uint8_t i;
	ADF_CLK_Clr;
	ADF_LE_Clr;
	for(i=0; i<32; i++)
	{
		if( dat & 0x80000000 )
			ADF_DATA_Set;
		else
			ADF_DATA_Clr;	
		dat <<= 1;		
		ADF_CLK_Set;
		ADF_CLK_Clr;	
	}
	ADF_LE_Set;	
}
